#include <stddef.h>

int				ft_strccmp(const char *s1, const char *s2, char c1, char c2)
{
	while (*s1 == *s2
		&& *s1 != '\0'
		&& *s1 != c1
		&& *s2 != c2)
	{
		s1++;
		s2++;
	}
	return ((unsigned char)(*s1 == c1 ? 0 : *s1)
		 - (unsigned char)(*s2 == c2 ? 0 : *s2));
}

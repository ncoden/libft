#include "libft.h"

t_pos		*ft_posnew(int x, int y)
{
	t_pos	 *new;

	if (!(new = (t_pos *)malloc(sizeof(t_pos))))
		return (NULL);
	new->x = x;
	new->y = y;
	return (new);
}
